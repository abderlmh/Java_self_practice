package m17_loops_part2;

public class Task2NestedLoop {

    public static void main(String[] args) {

        int givenNum  = 6;
        System.out.println();
        for (int i = 1; i <= givenNum; i++) {
            for (int j = 1; j <=givenNum ; j++) {
                System.out.print(" * ");
            }
            System.out.println();

        }


    }
}


