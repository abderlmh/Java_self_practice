package m18_loops_part3;

public class Rectangle3 {

    public static void main(String[] args) {

        System.out.println();
        for (int i = 1; i <= 8 ; i++) {

            for (int j = 1 ; j <= 8 ; j++) {
                System.out.print(" * ");
            }
            System.out.println();

        }

    }
}
