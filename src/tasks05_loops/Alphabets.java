package m18_loops_part3;

public class Alphabets {

    public static void main(String[] args) {

        for (char alphaC = 'A'; alphaC<='E';alphaC++){
            for (char alphaM = 'a';alphaM<='z';alphaM++){
                System.out.print(alphaC);
                System.out.print(alphaM+" ");
            }
            System.out.println();
        }
    }
}






